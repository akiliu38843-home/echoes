def _asof(anchors: list[tuple[str, float]], date: str) -> Optional[float]:
    """≤date 的最近一条；无（date 早于首锚）→ None。绝不取未来锚。"""
    pick = None
    for d0, v in anchors:           # 已升序
        if d0 <= date:
            pick = v
        else:
            break                   # 后面都晚于 date，停（no look-ahead）
    return pick


def struct_vals_asof(date: str) -> dict[str, float]:
    """返回 {var: 近似值}，仅含"该日已可知"的（≤date）。
    早于某变量首锚 / E1 pre-2010 → 该 var **缺失**（不编造、不顶替）。"""
    out: dict[str, float] = {}
    for var, anchors in _ANCHORS.items():
        v = _asof(anchors, date)
        if v is not None:
            out[var] = v
    return out


def coverage_note(date: str) -> str:
    have = struct_vals_asof(date)
    miss = [k for k in _ANCHORS if k not in have]
    return (f"{date}: 结构变量按当年取 {len(have)}/{len(_ANCHORS)} 个"
            f"（{PROVENANCE}）；缺失(早于首锚/规矩A留白){miss or '无'}")
